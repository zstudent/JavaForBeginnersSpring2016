package lesson160310;

public class Example05 {
	
	public static void main(String[] args) {
		
		int w = 10;
		int x = 10;
		int y = 10;
		int z = 10;
		
		System.out.println(w++);
		System.out.println(w);
		System.out.println();

		System.out.println(++x);
		System.out.println(x);
		System.out.println();
		
		System.out.println(y--);
		System.out.println(--z);
		
		w += 10;  // w = w + 10;
		
		y *= 20;
		
	}

}
